<script setup>
import InputError from '@/Components/InputError.vue';
import InputLabel from '@/Components/InputLabel.vue';
import PrimaryButton from '@/Components/PrimaryButton.vue';
import TextInput from '@/Components/TextInput.vue';
import { useForm } from '@inertiajs/vue3';
import { ref } from 'vue';

const props = defineProps({
    landingPage: Object,
    status: String,
});

const form = useForm({
    domain: props.landingPage.domain,
    // Add any other fields your landing page might have
});

const recentlySuccessful = ref(false);

const submit = () => {
    form.put(route('landing-pages.update', props.landingPage.id), {
        preserveScroll: true,
        onSuccess: () => {
            recentlySuccessful.value = true;
            setTimeout(() => recentlySuccessful.value = false, 2000);
        },
    });
};
</script>

<template>
    <section>
        <header>
            <h2 class="text-lg font-medium text-gray-900">Landing Page Information</h2>
            <p class="mt-1 text-sm text-gray-600">
                Update your landing page's information.
            </p>
        </header>

        <form @submit.prevent="submit" class="mt-6 space-y-6">
            <div>
                <InputLabel for="domain" value="Domain" />

                <TextInput
                    id="domain"
                    type="text"
                    class="mt-1 block w-full"
                    v-model="form.domain"
                    required
                    autofocus
                    autocomplete="domain"
                />

                <InputError class="mt-2" :message="form.errors.domain" />
            </div>

            <!-- Add more fields as needed for your landing page -->

            <div class="flex items-center gap-4">
                <PrimaryButton :disabled="form.processing">Save</PrimaryButton>

                <Transition
                    enter-active-class="transition ease-in-out"
                    enter-from-class="opacity-0"
                    leave-active-class="transition ease-in-out"
                    leave-to-class="opacity-0"
                >
                    <p v-if="recentlySuccessful" class="text-sm text-gray-600">Saved.</p>
                </Transition>
            </div>
        </form>
    </section>
</template>

